    private void btn_accionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accionActionPerformed
        try {
            if (mode.equals("INS")) {
                Transaccion t = new Transaccion();
                t.setIdUsuario(Integer.parseInt(txt_id_usuario.getText()));
                t.setMonto(Double.parseDouble(txt_monto.getText()));
                t.setDescripcion(txt_descripcion.getText());

                dao.insertar(t);
                parent.refrescarInfo();
                this.dispose();
            }

            if (mode.equals("UPD")) {
                Transaccion t = new Transaccion();
                t.setNumeroRecibo(Integer.parseInt(txt_id.getText()));
                t.setIdUsuario(Integer.parseInt(txt_id_usuario.getText()));
                t.setMonto(Double.parseDouble(txt_monto.getText()));
                t.setDescripcion(txt_descripcion.getText());

                dao.actualizar(t);
                parent.refrescarInfo();
                this.dispose();
            }

            if (mode.equals("DLT")) {
                dao.eliminar(numero_recibo);
                parent.refrescarInfo();
                this.dispose();
            }                                                                                           

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_btn_accionActionPerformed

