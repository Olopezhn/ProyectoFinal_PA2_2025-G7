    public FormularioTransacciones(ListadoTransacciones pParent, boolean modal, String pMode, int pNumeroRecibo) {
        super(pParent, modal);
        this.parent = pParent;
                        
        initComponents();

        dao = new TransaccionDAO();
        this.mode = pMode;
        this.numero_recibo = pNumeroRecibo;
        txt_id.setText(String.valueOf(pNumeroRecibo));
        txt_id.setEnabled(false);

        if (mode.equals("INS")) {
            setTitle("Formulario Insertar");
            btn_accion.setText("Insertar");
        } else if (mode.equals("UPD")) {
            setTitle("Formulario Actualizar");
            Transaccion t = dao.getById(numero_recibo);
            txt_id_usuario.setText(String.valueOf(t.getIdUsuario()));
            txt_monto.setText(String.valueOf(t.getMonto()));
            txt_descripcion.setText(t.getDescripcion());
            btn_accion.setText("Actualizar");
        } else if (mode.equals("DLT")) {
            setTitle("Formulario Eliminar");
            Transaccion t = dao.getById(numero_recibo);
            txt_id_usuario.setText(String.valueOf(t.getIdUsuario()));
            txt_monto.setText(String.valueOf(t.getMonto()));
            txt_descripcion.setText(t.getDescripcion());

            txt_id_usuario.setEnabled(false);
            txt_monto.setEnabled(false);
            txt_descripcion.setEnabled(false);
            btn_accion.setText("Eliminar");
        }

        lbl_info.setText("100 por d√≠a atrasado");
    }

